def update_landing_command_palette(posts: list[Post]) -> None:
    landing_path = ROOT / "index.html"
    source = landing_path.read_text(encoding="utf-8")

    start_marker = "<!-- cmd-palette-start -->"
    end_marker = "<!-- cmd-palette-end -->"
    start = source.find(start_marker)
    end = source.find(end_marker)

    if start == -1 or end == -1:
        return

    command_items = build_command_items_for_landing(posts)

    items_html = []
    for item in command_items:
        items_html.append(
            f'            <a class="cmd-result" href="{html.escape(item["href"], quote=True)}" '
            f'data-cmd-item data-search="{html.escape(item["search"], quote=True)}">\n'
            f'                <span class="cmd-result-label">{html.escape(item["label"])}</span>\n'
            f'                <span class="cmd-result-meta">{html.escape(item["meta"])}</span>\n'
            f'            </a>'
        )

    palette_html = (
        '    <div id="cmd-layer">\n'
        '        <input type="text" id="cmd-input" class="cmd-input" placeholder="Jump somewhere..." autocomplete="off" spellcheck="false" />\n'
        '        <div class="cmd-response" id="cmd-response">\n'
        + "\n".join(items_html) + "\n"
        '            <p class="cmd-empty" id="cmd-empty" hidden>Can\'t match that one. But it\'s probably here.</p>\n'
        '        </div>\n'
        '    </div>'
    )

    new_source = (
        source[:start]
        + start_marker + "\n"
        + palette_html + "\n    "
        + end_marker
        + source[end + len(end_marker):]
    )
    landing_path.write_text(new_source, encoding="utf-8")


def build_site() -> list[Post]:
    BLOG_DIR.mkdir(parents=True, exist_ok=True)
    POSTS_DIR.mkdir(parents=True, exist_ok=True)

    posts = load_posts()
    profile = load_landing_profile()
    write_output(BLOG_DIR / "index.html", render_index(posts, profile))
    write_output(BLOG_DIR / "archive.html", render_archive(posts, profile))

    active_post_paths: set[Path] = set()
    for post in posts:
        post_output_path = POSTS_DIR / f"{post.slug}.html"
        active_post_paths.add(post_output_path)
        write_output(post_output_path, render_post_page(post, posts, profile))

    for existing_post in POSTS_DIR.glob("*.html"):
        if existing_post not in active_post_paths:
            existing_post.unlink()

    update_landing_command_palette(posts)

    return posts


def create_new_post(title: str, requested_date: date | None = None) -> Path:
    post_date = requested_date or date.today()
    base_slug = slugify(title)
    filename = f"{post_date.isoformat()}-{base_slug}.md"
    target = CONTENT_DIR / filename
    suffix = 2

    while target.exists():
        target = CONTENT_DIR / f"{post_date.isoformat()}-{base_slug}-{suffix}.md"
        suffix += 1

    template = f"""---
title: {title}
date: {post_date.isoformat()}
summary: One line that will show up on the blog homepage.
tags: notes, draft
featured: false
---
Start writing here.
"""
    target.write_text(template, encoding="utf-8")
    return target


def serve_site(port: int) -> None:
    build_site()

    class BlogHandler(SimpleHTTPRequestHandler):
        def __init__(self, *args, **kwargs):
            super().__init__(*args, directory=str(ROOT), **kwargs)

        def do_GET(self) -> None:  # noqa: N802
            try:
                build_site()
            except Exception as exc:  # pragma: no cover - manual workflow helper
                self.send_response(500)
                self.send_header("Content-Type", "text/plain; charset=utf-8")
                self.end_headers()
                self.wfile.write(str(exc).encode("utf-8"))
                return
            super().do_GET()

    server = ThreadingHTTPServer(("127.0.0.1", port), BlogHandler)
    print(f"Serving on http://127.0.0.1:{port}/blog/")
    try:
        server.serve_forever()
    except KeyboardInterrupt:
        print("\nStopping server.")
    finally:
        server.server_close()


def build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(
        description="Scaffold, build, and preview the blog beside index.html."
    )
    subparsers = parser.add_subparsers(dest="command", required=True)

    build_parser = subparsers.add_parser("build", help="Generate the blog HTML files.")
    build_parser.set_defaults(command_name="build")

    new_parser = subparsers.add_parser("new", help="Create a new markdown post.")
    new_parser.add_argument("title", help="Title for the new post")
    new_parser.add_argument(
        "--date",
        dest="date_value",
        type=date.fromisoformat,
        help="Optional publish date in YYYY-MM-DD format",
    )

    serve_parser = subparsers.add_parser(
        "serve",
        help="Build the blog and serve the whole site locally.",
    )
    serve_parser.add_argument(
        "--port",
        type=int,
        default=8000,
        help="Port to serve on. Defaults to 8000.",
    )

    return parser


def main() -> int:
    CONTENT_DIR.mkdir(parents=True, exist_ok=True)
    parser = build_parser()
    args = parser.parse_args()

    try:
        if args.command == "build":
            posts = build_site()
            print(f"Built {len(posts)} post(s) into {BLOG_DIR.relative_to(ROOT)}.")
            return 0

        if args.command == "new":
            target = create_new_post(args.title, args.date_value)
            print(target.relative_to(ROOT))
            return 0

        if args.command == "serve":
            serve_site(args.port)
            return 0
    except Exception as exc:  # pragma: no cover - surfaced to CLI
        print(f"Error: {exc}", file=sys.stderr)
        return 1

    return 0


if __name__ == "__main__":
    raise SystemExit(main())
